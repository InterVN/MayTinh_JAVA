    private void jButton35ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton35ActionPerformed
            num2 = Double.parseDouble(txtonhap.getText());
            
            if(opr == "+")
            {
                result = num1 + num2;
                txtonhap.setText(Double.toString(result));
            }
            else if(opr == "-"){
                 result = num1 - num2;
                txtonhap.setText(Double.toString(result));
            }
            else if(opr == "*"){
                 result = num1 * num2;
                txtonhap.setText(Double.toString(result));
            }
            else if(opr == "/"){
                 result = num1 / num2;
                txtonhap.setText(Double.toString(result));
            }
            else if(opr == "%"){
                 result = num1 % num2;
                txtonhap.setText(Double.toString(result));
            }
                    
    }//GEN-LAST:event_jButton35ActionPerformed

